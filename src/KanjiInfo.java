public class KanjiInfo {

    private String kanji, unicode, grade, stroke_count, freq, jlpt, meaning, on, kun, nanori;

    public KanjiInfo() {
    }

    public String getKanji() {
        return kanji;
    }

    public void setKanji(String kanji) {
        this.kanji = kanji;
    }

    public String getUnicode() { return unicode; }

    public void setUnicode(String unicode) { this.unicode = unicode; }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getStroke_count() {
        return stroke_count;
    }

    public void setStroke_count(String stroke_count) {
        this.stroke_count = stroke_count;
    }

    public String getFreq() {
        return freq;
    }

    public void setFreq(String freq) {
        this.freq = freq;
    }

    public String getJlpt() {
        return jlpt;
    }

    public void setJlpt(String jlpt) {
        this.jlpt = jlpt;
    }

    public String getMeaning() {
        return meaning;
    }

    public void setMeaning(String meaning) {
        this.meaning = meaning;
    }

    public String getOn() {
        return on;
    }

    public void setOn(String on) {
        this.on = on;
    }

    public String getKun() {
        return kun;
    }

    public void setKun(String kun) {
        this.kun = kun;
    }

    public String getNanori() {
        return nanori;
    }

    public void setNanori(String nanori) {
        this.nanori = nanori;
    }

}
